library(dplyr)
rm(list=ls())
uk_product_data <- read.csv('exp_uk_product_data.csv',stringsAsFactors = FALSE)
xi_fe <- read.csv('exp_xi_fe.csv',stringsAsFactors = FALSE)
uk_product_data <- cbind(uk_product_data,xi_fe)
python_image_table <- read.csv('python_image_table.csv',stringsAsFactors = FALSE)
uk_product_data <- uk_product_data %>% select(Genmodel_ID=clustering_ids,xi_fe)
uk_product_data$xi_fe <- round(uk_product_data$xi_fe,3)
uk_product_data <- uk_product_data %>% distinct()
python_image_table <- merge(python_image_table,uk_product_data)
write.csv(python_image_table,'exp_python_image_table.csv',row.names=FALSE)

